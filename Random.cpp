#include "Random.h"


//------------------------------------------------------------------------------
void XorShift32::setState (uint32_t lowSeed, uint32_t highSeed) {
	_x= highSeed;
	_y= lowSeed;
	for (unsigned j=0; j<6; ++j)
		next();
}

//------------------------------------------------------------------------------
//return uniform random ub4 in range [0 .. 2^32-1]
XorShift32& XorShift32::next () {
	//here a,b,c= 3,-13,11
	uint32_t t = _x^(_x<<3);
	_x = _y;
	_y = t^(t>>13)^_y^(_y<<11);
   return *this;
}


